    // -----------------------------------------------------------------------
    // Pipe / Hose builder
    // A run of straight legs from a start point. Each leg becomes a rotated
    // content cylinder (+ wall annulus if wall > 0). After the first leg,
    // each leg is extended backward by the outer radius so corners have no
    // void at the bend. Total activity is split across legs by volume.
    // -----------------------------------------------------------------------
    const PIPE_DIRS = {
        '+Y': { v: [0, 1, 0],  rot: { x: 0,    y: 0, z: 0 },   label: 'Up (+Y)' },
        '-Y': { v: [0, -1, 0], rot: { x: 180,  y: 0, z: 0 },   label: 'Down (-Y)' },
        '+X': { v: [1, 0, 0],  rot: { x: 0,    y: 0, z: -90 }, label: '+X (east)' },
        '-X': { v: [-1, 0, 0], rot: { x: 0,    y: 0, z: 90 },  label: '-X (west)' },
        '+Z': { v: [0, 0, 1],  rot: { x: 90,   y: 0, z: 0 },   label: '+Z (south)' },
        '-Z': { v: [0, 0, -1], rot: { x: -90,  y: 0, z: 0 },   label: '-Z (north)' }
    };

    function buildPipe(cfg) {
        const legs = (cfg.legs || []).filter(l => PIPE_DIRS[l.dir] && l.length > 0);
        if (!legs.length) { status('Pipe needs at least one leg'); return; }

        const outerR = cfg.outerR;
        const wall = Math.max(0, Math.min(cfg.wall || 0, outerR * 0.95));
        const contentR = outerR - wall;
        const name = cfg.name || 'Pipe';

        pushUndo();

        // Effective geometry per leg (with corner back-extension)
        const built = [];
        let cursor = { ...cfg.start };
        legs.forEach((leg, i) => {
            const d = PIPE_DIRS[leg.dir];
            const ext = i > 0 ? outerR : 0;
            const pos = {
                x: cursor.x - d.v[0] * ext,
                y: cursor.y - d.v[1] * ext,
                z: cursor.z - d.v[2] * ext
            };
            built.push({ pos, rot: d.rot, len: leg.length + ext });
            cursor = {
                x: cursor.x + d.v[0] * leg.length,
                y: cursor.y + d.v[1] * leg.length,
                z: cursor.z + d.v[2] * leg.length
            };
        });

        const totalContentVol = built.reduce(
            (s, b) => s + Math.PI * contentR * contentR * b.len, 0);

        built.forEach((b, i) => {
            const contentCfg = {
                role: cfg.source ? 'source' : 'shield',
                materialKey: cfg.contentsMaterial || 'water',
                position: b.pos,
                rotation: b.rot,
                priority: 30,
                label: name + ' leg ' + (i + 1),
                dimensions: { radius: contentR, height: b.len }
            };
            if (cfg.source) {
                contentCfg.isotopeKey = cfg.source.isotopeKey || 'Co-60';
                contentCfg.activity_Ci = (cfg.source.activity_Ci || 0) *
                    (Math.PI * contentR * contentR * b.len) / totalContentVol;
            }
            model.addVolume(new Geometry.CylinderVolume(contentCfg));

            if (wall > 0) {
                model.addVolume(new Geometry.AnnulusVolume({
                    role: 'shield',
                    materialKey: cfg.wallMaterial || 'steel',
                    position: { ...b.pos },
                    rotation: { ...b.rot },
                    priority: 60,
                    label: name + ' wall ' + (i + 1),
                    dimensions: { innerRadius: contentR, outerRadius: outerR, height: b.len }
                }));
            }
        });

        refreshAll(false);
        status('Built ' + name + ': ' + legs.length + ' leg(s), ends at (' +
            (cursor.x / IN).toFixed(1) + '", ' + (cursor.y / IN).toFixed(1) + '", ' +
            (cursor.z / IN).toFixed(1) + '")');
        return cursor;
    }

