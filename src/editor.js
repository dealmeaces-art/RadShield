    function applyProp(field, rawValue) {
        const vol = selection ? model.getVolume(selection) : null;
        if (!vol) return;
        pushUndo();

        const numVal = parseFloat(rawValue);
        switch (field) {
            case 'label': vol.label = rawValue; break;
            case 'role':
                vol.role = rawValue;
                if (rawValue === 'source') {
                    vol.isotopeKey = vol.isotopeKey || 'Co-60';
                    vol.activity_Ci = vol.activity_Ci || 1;
                }
                break;
            case 'materialKey': vol.materialKey = rawValue; break;
            case 'isotopeKey': vol.isotopeKey = rawValue; break;
            case 'activity_Ci': vol.activity_Ci = numVal || 0; break;
            case 'priority': vol.priority = Math.round(numVal) || 0; break;
            case 'posX': vol.position.x = numVal * IN; break;
            case 'posY': vol.position.y = numVal * IN; break;
            case 'posZ': vol.position.z = numVal * IN; break;
            case 'rotX': vol.setRotation({ ...vol.rotation, x: numVal }); break;
            case 'rotY': vol.setRotation({ ...vol.rotation, y: numVal }); break;
            case 'rotZ': vol.setRotation({ ...vol.rotation, z: numVal }); break;
            case 'radius': vol.radius = numVal * IN; break;
            case 'height': vol.height = numVal * IN; break;
            case 'thickness': vol.thickness = numVal * IN; vol.height = numVal * IN; break;
            case 'innerRadius': vol.innerRadius = numVal * IN; break;
            case 'outerRadius': vol.outerRadius = numVal * IN; break;
            case 'width': vol.width = numVal * IN; break;
            case 'depth': vol.depth = numVal * IN; break;
        }
        refreshAll(false);
    }

