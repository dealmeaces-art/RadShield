    function renderPipeLegs() {
        const list = document.getElementById('pipeLegList');
        if (!list) return;
        list.innerHTML = '';
        pipeLegs.forEach((leg, i) => {
            const div = document.createElement('div');
            div.className = 'layer-item';
            const dirOpts = Object.entries(PIPE_DIRS).map(([k, d]) =>
                `<option value="${k}" ${leg.dir === k ? 'selected' : ''}>${d.label}</option>`).join('');
            div.innerHTML =
                `<span style="color:#8b949e; min-width:16px;">${i + 1}.</span>` +
                `<select style="width:110px" onchange="Editor._setPipeLeg(${i},'dir',this.value)">${dirOpts}</select>` +
                `<input type="number" value="${leg.length}" step="1" min="0.1"
                     onchange="Editor._setPipeLeg(${i},'length',parseFloat(this.value))">` +
                `<span class="unit">in</span>` +
                `<button class="remove-btn" onclick="Editor._removePipeLeg(${i})">&times;</button>`;
            list.appendChild(div);
        });
    }

